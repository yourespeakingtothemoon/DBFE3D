int main(int argc, char** argv)
{
	LOG("Application Started...");

	dbf::initializeMemory();
	dbf::SetFilePath("../Assets");

	dbf::Engine::instance().init();
	dbf::Engine::instance().reg();
	LOG("Engine Initialized...");

	//create window
	dbf::g_renderer.generateWindow("DBF3", 800, 600);
	LOG("Window Generated...");

	// create vertex buffer
	GLuint vbo = 0;
	glGenBuffers(1, &vbo);
	glBindBuffer(GL_ARRAY_BUFFER, vbo);
	glBufferData(GL_ARRAY_BUFFER, sizeof(vertices), vertices, GL_STATIC_DRAW);

	// create vertex array
	GLuint vao = 0;
	glGenVertexArrays(1, &vao);
	glBindVertexArray(vao);

	glBindBuffer(GL_ARRAY_BUFFER, vbo);

	glEnableVertexAttribArray(0); //pos
	glVertexAttribPointer(0, 3, GL_FLOAT, GL_FALSE, 8 * sizeof(float), (void*)0);

	glEnableVertexAttribArray(1); //color
	glVertexAttribPointer(1, 3, GL_FLOAT, GL_FALSE, 8 * sizeof(float), (void*)(3 * sizeof(float)));

	glEnableVertexAttribArray(2); //coord
	glVertexAttribPointer(2, 2, GL_FLOAT, GL_FALSE, 8 * sizeof(float), (void*)(6 * sizeof(float)));

	//generate program
	std::shared_ptr<dbf::Program> program = dbf::g_resourceManager.Get<dbf::Program>("Shaders/basic.prog");
	program->Link();
	program->Use();

	// create material 
	std::shared_ptr<dbf::Material> material = dbf::g_resourceManager.Get<dbf::Material>("Materials/box.mtrl");
	material->Bind();

	material->GetProgram()->SetUniform("tint", glm::vec3{ 1, 0, 0 });
	material->GetProgram()->SetUniform("scale", 0.5f);

	glm::mat4 mx{ 1 };

	bool quit = false;

	while (!quit)
	{
		dbf::Engine::instance().update();
		if (dbf::g_inputSystem.queryKeyState(dbf::key_escape) == dbf::InpSystem::keyState::Pressed) 
		{ quit = true; }

		material->GetProgram()->SetUniform("scale", std::sin(dbf::g_time.time * 3));
		material->GetProgram()->SetUniform("transform", mx);

		dbf::g_renderer.openFrame();

		glDrawArrays(GL_TRIANGLES, 0, 36);

		dbf::g_renderer.closeFrame();
	}
	dbf::Engine::instance().shutdown();

	return 0;
}